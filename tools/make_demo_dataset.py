"""Create a tiny synthetic TRACE-SAM-SR demo dataset."""
from __future__ import annotations

import argparse
import csv
import math
import shutil
from pathlib import Path

import numpy as np
from PIL import Image, ImageDraw


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--out", default="demo_data")
    p.add_argument("--image-size", "--image_size", dest="image_size", type=int, default=64)
    p.add_argument("--train-count", type=int, default=6)
    p.add_argument("--val-count", type=int, default=2)
    p.add_argument("--test-count", type=int, default=2)
    p.add_argument("--overwrite", action="store_true")
    return p.parse_args()


def _texture(size: int, seed: int) -> np.ndarray:
    rng = np.random.default_rng(seed)
    yy, xx = np.mgrid[0:size, 0:size]
    base = 176 + 18 * np.sin(xx / 7.0 + seed) + 12 * np.cos(yy / 9.0)
    speckle = rng.normal(0, 10, (size, size))
    arr = np.clip(base + speckle, 80, 235).astype(np.uint8)
    rgb = np.stack([arr + 6, arr + 2, arr - 4], axis=-1)
    return np.clip(rgb, 0, 255).astype(np.uint8)


def _crack_points(size: int, seed: int) -> list[tuple[int, int]]:
    rng = np.random.default_rng(seed)
    x0 = int(rng.integers(size // 8, size // 3))
    y0 = int(rng.integers(size // 5, size // 2))
    pts = []
    for i in range(8):
        t = i / 7.0
        x = x0 + int(t * size * 0.62 + 4 * math.sin(seed + t * math.pi * 2))
        y = y0 + int((t - 0.5) * size * 0.24 + 5 * math.sin(seed * 0.7 + t * math.pi * 3))
        pts.append((int(np.clip(x, 2, size - 3)), int(np.clip(y, 2, size - 3))))
    return pts


def _write_pair(image_path: Path, label_path: Path, size: int, seed: int) -> None:
    image_path.parent.mkdir(parents=True, exist_ok=True)
    label_path.parent.mkdir(parents=True, exist_ok=True)
    img = Image.fromarray(_texture(size, seed))
    mask = Image.new("L", (size, size), 0)
    crack = _crack_points(size, seed)
    draw_img = ImageDraw.Draw(img)
    draw_mask = ImageDraw.Draw(mask)
    width = 1 + (seed % 2)
    draw_img.line(crack, fill=(35, 32, 30), width=width + 1, joint="curve")
    draw_mask.line(crack, fill=255, width=width + 1, joint="curve")
    if seed % 3 == 0:
        branch = [crack[3], (crack[3][0] + size // 8, crack[3][1] - size // 7)]
        draw_img.line(branch, fill=(45, 42, 40), width=width, joint="curve")
        draw_mask.line(branch, fill=255, width=width, joint="curve")
    img.save(image_path)
    mask.save(label_path)


def _write_hr(image_path: Path, size: int, seed: int) -> None:
    image_path.parent.mkdir(parents=True, exist_ok=True)
    img = Image.fromarray(_texture(size, seed))
    draw = ImageDraw.Draw(img)
    if seed % 2 == 0:
        draw.line(_crack_points(size, seed + 31), fill=(38, 36, 35), width=1)
    img.save(image_path)


def _write_readme(out: Path) -> None:
    (out / "README.md").write_text(
        "# TRACE-SAM-SR Demo Data\n\n"
        "Synthetic concrete-like images and binary crack masks for software smoke tests. "
        "This data is generated by `tools/make_demo_dataset.py` and must not be used "
        "for reporting model performance.\n",
        encoding="utf-8",
    )


def main() -> None:
    args = parse_args()
    out = Path(args.out)
    if out.exists():
        if not args.overwrite:
            raise FileExistsError(f"{out} exists; pass --overwrite to replace it")
        shutil.rmtree(out)
    rows: list[dict[str, str | int]] = []
    size = int(args.image_size)
    split_counts = {"train": int(args.train_count), "val": int(args.val_count), "test": int(args.test_count)}
    for split, count in split_counts.items():
        for i in range(count):
            name = f"demo_{split}_{i:03d}.png"
            _write_pair(out / "bridge_crack" / split / "image" / name, out / "bridge_crack" / split / "label" / name, size, seed=1000 + i + 17 * len(split))
            rows.append({"dataset": "bridge_crack", "split": split, "image": name, "label": name, "image_size": size})
    for split, count in {"train": max(4, args.train_count), "val": max(2, args.val_count)}.items():
        for i in range(count):
            name = f"cement_{split}_{i:03d}.png"
            _write_hr(out / "country_cement" / split / "image" / name, size, seed=2000 + i + 13 * len(split))
            rows.append({"dataset": "country_cement", "split": split, "image": name, "label": "", "image_size": size})
    with open(out / "manifest.csv", "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=["dataset", "split", "image", "label", "image_size"])
        writer.writeheader()
        writer.writerows(rows)
    _write_readme(out)
    print(f"Wrote demo dataset to {out} ({len(rows)} records)")


if __name__ == "__main__":
    main()
